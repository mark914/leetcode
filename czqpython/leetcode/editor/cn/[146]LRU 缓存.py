class LRUCache(object):

    class LRUCache:

        def __init__(self, capacity: int):
            self.capacity = capacity
            self.cache = DoubleList()
            self.map = dict()

        def get(self, key: int) -> int:
            if key not in self.map:
                return -1
            x = self.map[key]
            # make most recently
            self.makeRecently(key)
            return x.val

        def put(self, key: int, value: int) -> None:
            if key in self.map:
                x = self.map[key]
                # delete x and put x recently
                self.delete(key)
                # add key val
                self.addRecent(key, value)
                return

            if self.capacity == len(self.map):
                self.deleteLeastRecently()
            self.addRecent(key, value)

        def makeRecently(self, key):
            x = self.map[key]
            self.cache.remove(x)
            self.cache.addLast(x)

        def delete(self, key):
            x = self.map[key]
            self.cache.remove(x)
            self.map.pop(key)

        def addRecent(self, key, val):
            x = Node(key, val)
            self.cache.addLast(x)
            self.map[key] = x

        def deleteLeastRecently(self):
            # delete first
            first = self.cache.removeFirst()
            key = first.key
            self.map.pop(key)

class Node:
    def __init__(self, k, v):
        self.key = k
        self.val = v
        self.pre = None
        self.next = None

class DoubleList:
    def __init__(self):
        self.head = Node(0, 0)
        self.tail = Node(0, 0)
        self.head.next = self.tail
        self.tail.pre = self.head
        self.size = 0

    def addLast(self, x):
        x.pre = self.tail.pre
        x.next = self.tail
        self.pre.next = x
        self.tail.pre = x
        self.size += 1

    def remove(self, x):
        x.pre.next = x.next
        x.next.pre = x.pre
        self.size -= 1

    def removeFirst(self):
        if self.head.next == self.tail:
            return None
        first = self.head.next
        self.remove(first)
        return first

