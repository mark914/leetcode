class Solution(object):
    def permuteUnique(self, nums):
        """
        :type nums: List[int]
        :rtype: List[List[int]]
        """
        res = []
        path = []
        nums.sort()
        used = [0] * len(nums)

        def dfs():
            if len(path) == len(nums):
                res.append(path[:])
                print(path)
                return
            for i in range(len(nums)):
                if used[i]:
                    continue
                if i > 0 and nums[i] == nums[i - 1] and not used[i]:
                    continue

                path.append(nums[i])
                used[i] = 1
                dfs()
                used[i] = 0
                path.pop()

        dfs()
        return res
